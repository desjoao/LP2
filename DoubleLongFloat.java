public class DoubleLongFloat {
	public static void main(String[] args) {
		float f = 5.0F;
		long l = 100000000L;
		double d = 5.123456789;
		

		System.out.println("Exemplo de tipo de dado 'float': " + f);
		System.out.println("Exemplo de tipo de dado 'long': " + l);
		System.out.println("Exemplo de tipo de dado 'double': " + d);
	}
}

